using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ChildCareLicensingInspections
{
    public class Inspection
    {
        public int InspectionID { get; set; }
        public int FacilityLicID { get; set; }
        public DateTime InspectionStart { get; set; }
        public DateTime InspectionEnd { get; set; }
        public int NumViolations { get; set; }
        public string Page { get; set; }

        public Inspection(int facilityLicID, int inspectionID)
        {
            FacilityLicID = facilityLicID;
            InspectionID = inspectionID;
        }
    }
}
